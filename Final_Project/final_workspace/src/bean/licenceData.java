package bean;

public class licenceData {
	private String licenceName;
	private String grade;
	private int buyPoint;
	private String licenceLimit;
	private String content;
	private int addGood;
	private int addPoint;
	private String imgName;
	private int levelLimit;
	
	public String getLicenceName() {
		return licenceName;
	}
	public void setLicenceName(String licenceName) {
		this.licenceName = licenceName;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public int getBuyPoint() {
		return buyPoint;
	}
	public void setBuyPoint(int buyPoint) {
		this.buyPoint = buyPoint;
	}
	public String getLicenceLimit() {
		return licenceLimit;
	}
	public void setLicenceLimit(String licenceLimit) {
		this.licenceLimit = licenceLimit;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getAddGood() {
		return addGood;
	}
	public void setAddGood(int addGood) {
		this.addGood = addGood;
	}
	public int getAddPoint() {
		return addPoint;
	}
	public void setAddPoint(int addPoint) {
		this.addPoint = addPoint;
	}
	public String getImgName() {
		return imgName;
	}
	public void setImgName(String imgName) {
		this.imgName = imgName;
	}
	public int getLevelLimit() {
		return levelLimit;
	}
	public void setLevelLimit(int levelLimit) {
		this.levelLimit = levelLimit;
	}
	
	
}
